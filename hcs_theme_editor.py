@dataclass
class HCSDocument:
    path: Path
    lines: list[str]

    @classmethod
    def load(cls, path: str | Path) -> "HCSDocument":
        target = Path(path)
        return cls(target, target.read_text(encoding="utf-8").splitlines())

    @classmethod
    def new_overlay(cls, scheme_name: str = DEFAULT_NEW_THEME_NAME, path: str | Path | None = None) -> "HCSDocument":
        name = scheme_name.strip() or DEFAULT_NEW_THEME_NAME
        target = Path(path) if path is not None else Path(f"{name}.hcs")
        return cls(target, build_overlay_template_lines(name))

    def __post_init__(self) -> None:
        self._reindex()

    def _reindex(self) -> None:
        self.key_to_index: dict[str, int] = {}
        self.key_to_meta: dict[str, tuple[str, str]] = {}
        self.key_to_value: dict[str, str] = {}
        for index, line in enumerate(self.lines):
            stripped = line.lstrip()
            if stripped.startswith("//") or stripped.startswith("#include"):
                continue
            match = LINE_RE.match(line)
            if not match:
                continue
            key = match.group("key")
            value = match.group("value").strip()
            comment = match.group("comment") or ""
            indent = match.group("indent") or ""
            self.key_to_index[key] = index
            self.key_to_meta[key] = (indent, comment)
            self.key_to_value[key] = value

    def get_color(self, key: str) -> tuple[float, float, float] | None:
        value = self.key_to_value.get(key)
        if value is None:
            return None
        return parse_color_value(value)

    def get_value(self, key: str) -> str | None:
        return self.key_to_value.get(key)

    def set_value(self, key: str, value: str) -> None:
        if key in self.key_to_index:
            indent, comment = self.key_to_meta.get(key, ("", ""))
            self.lines[self.key_to_index[key]] = f"{indent}{key}: {value}{comment}"
        else:
            self.lines.append(f"{key}: {value}")
        self._reindex()

    def set_color(self, key: str, rgb: tuple[float, float, float]) -> None:
        value = format_rgb(rgb)
        if key in self.key_to_index:
            indent, comment = self.key_to_meta.get(key, ("", ""))
            self.lines[self.key_to_index[key]] = f"{indent}{key}: {value}{comment}"
        else:
            self.lines.append(f"{key}: {value}")
        self._reindex()

    def save(self, path: str | Path | None = None) -> None:
        target = Path(path) if path is not None else self.path
        target.write_text(self.render_text(), encoding="utf-8")
        self.path = target

    def render_text(self) -> str:
        return "\n".join(self.lines) + "\n"

    def write_to(self, path: str | Path) -> Path:
        target = Path(path)
        target.write_text(self.render_text(), encoding="utf-8")
        return target

    def sync_scheme_name_to_path(self, path: str | Path) -> str:
        target = Path(path)
        scheme_name = target.stem.strip() or "CustomTheme"
        self.set_value("Scheme", scheme_name)
        return scheme_name


class ScrollableFrame(tk.Frame):
    def __init__(self, master: tk.Misc, bg: str) -> None:
        super().__init__(master, bg=bg)
        self.canvas = tk.Canvas(self, bg=bg, highlightthickness=0, bd=0)
        self.scrollbar = tk.Scrollbar(self, orient="vertical", command=self.canvas.yview)
        self.inner = tk.Frame(self.canvas, bg=bg)

        self.inner.bind(
            "<Configure>",
            lambda event: self.canvas.configure(scrollregion=self.canvas.bbox("all")),
        )
        self.canvas.bind(
            "<Configure>",
            lambda event: self.canvas.itemconfigure(self.window_id, width=event.width),
        )

        self.window_id = self.canvas.create_window((0, 0), window=self.inner, anchor="nw")
        self.canvas.configure(yscrollcommand=self.scrollbar.set)

        self.canvas.pack(side="left", fill="both", expand=True)
        self.scrollbar.pack(side="right", fill="y")

        self.canvas.bind_all("<MouseWheel>", self._on_mousewheel)

    def _on_mousewheel(self, event: tk.Event) -> None:
        try:
            self.canvas.yview_scroll(int(-1 * (event.delta / 120)), "units")
        except tk.TclError:
            pass


