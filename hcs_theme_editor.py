def parse_houdini_dir_version(name: str) -> tuple[int, ...]:
    lowered = name.lower()
    if not lowered.startswith("houdini"):
        return ()
    parts = re.findall(r"\d+", lowered[len("houdini"):])
    if not parts:
        return ()
    return tuple(int(part) for part in parts)


def find_houdini_config_dirs() -> list[Path]:
    candidates: list[Path] = []
    seen: set[str] = set()

    env_pref_dir = os.environ.get("HOUDINI_USER_PREF_DIR", "").strip()
    if env_pref_dir:
        config_dir = Path(env_pref_dir).expanduser() / "config"
        resolved = str(config_dir)
        if resolved not in seen:
            candidates.append(config_dir)
            seen.add(resolved)

    documents_dir = Path.home() / "Documents"
    if documents_dir.exists():
        for child in documents_dir.iterdir():
            if not child.is_dir():
                continue
            if not child.name.lower().startswith("houdini"):
                continue
            config_dir = child / "config"
            resolved = str(config_dir)
            if resolved in seen:
                continue
            candidates.append(config_dir)
            seen.add(resolved)

    return sorted(
        candidates,
        key=lambda path: (
            parse_houdini_dir_version(path.parent.name),
            path.parent.stat().st_mtime if path.parent.exists() else 0,
        ),
        reverse=True,
    )


