LINE_RE = re.compile(
    r"^(?P<indent>\s*)(?P<key>[A-Za-z0-9_]+)\s*:\s*(?P<value>.*?)(?P<comment>\s*//.*)?$"
)
DEFAULT_NEW_THEME_NAME = "CustomTheme"


def build_overlay_template_lines(scheme_name: str) -> list[str]:
    name = scheme_name.strip() or DEFAULT_NEW_THEME_NAME
    return [
        "// Generated by Houdini Theme Editor.",
        "// This file contains only user-authored overrides.",
        "// It references Houdini's local dark theme and does not bundle SideFX files.",
        '#include "$HFS/houdini/config/UIDark.hcs"',
        f"Scheme: {name}",
        "",
    ]


def clamp01(value: float) -> float:
    return max(0.0, min(1.0, value))


def rgb_to_hex(rgb: tuple[float, float, float]) -> str:
    r, g, b = (round(clamp01(channel) * 255) for channel in rgb)
    return f"#{r:02X}{g:02X}{b:02X}"


def hex_to_rgb(value: str) -> tuple[float, float, float]:
    text = value.strip().lstrip("#")
    if len(text) == 6:
        return tuple(int(text[index:index + 2], 16) / 255.0 for index in (0, 2, 4))  # type: ignore[return-value]
    raise ValueError(f"Unsupported hex color: {value}")


def format_rgb(rgb: tuple[float, float, float]) -> str:
    return " ".join(f"{clamp01(channel):.3f}".rstrip("0").rstrip(".") or "0" for channel in rgb)


def parse_float_triplet(value: str) -> tuple[float, float, float] | None:
    parts = value.strip().split()
    if len(parts) != 3:
        return None
    try:
        return tuple(clamp01(float(part)) for part in parts)  # type: ignore[return-value]
    except ValueError:
        return None


def parse_hsv(value: str) -> tuple[float, float, float] | None:
    parts = value.strip().split()
    if len(parts) != 4 or parts[0].upper() != "HSV":
        return None
    try:
        hue = float(parts[1]) / 360.0
        sat = clamp01(float(parts[2]))
        val = clamp01(float(parts[3]))
    except ValueError:
        return None
    return colorsys.hsv_to_rgb(hue, sat, val)


def parse_grey(value: str) -> tuple[float, float, float] | None:
    match = re.fullmatch(r"GREY\(([^)]+)\)", value.strip(), re.IGNORECASE)
    if not match:
        return None
    try:
        grey = clamp01(float(match.group(1)))
    except ValueError:
        return None
    return (grey, grey, grey)


def parse_hex_variants(value: str) -> tuple[float, float, float] | None:
    text = value.strip()
    if not text.startswith("#"):
        return None
    compact = text[1:].replace(" ", "")
    if len(compact) != 6:
        return None
    return hex_to_rgb("#" + compact)


def parse_named_color(value: str) -> tuple[float, float, float] | None:
    text = value.strip().upper()
    if text == "BLACK":
        return (0.0, 0.0, 0.0)
    if text == "WHITE":
        return (1.0, 1.0, 1.0)
    return None


def parse_color_value(value: str) -> tuple[float, float, float] | None:
    for parser in (parse_float_triplet, parse_hsv, parse_grey, parse_hex_variants, parse_named_color):
        parsed = parser(value)
        if parsed is not None:
            return parsed
    return None


def adjust_hsv(
    rgb: tuple[float, float, float],
    *,
    hue_shift: float = 0.0,
    sat_scale: float = 1.0,
    val_scale: float = 1.0,
    sat_offset: float = 0.0,
    val_offset: float = 0.0,
) -> tuple[float, float, float]:
    hue, sat, val = colorsys.rgb_to_hsv(*rgb)
    hue = (hue + hue_shift) % 1.0
    sat = clamp01(sat * sat_scale + sat_offset)
    val = clamp01(val * val_scale + val_offset)
    return colorsys.hsv_to_rgb(hue, sat, val)


def build_channel_family_palette(base_rgb: tuple[float, float, float]) -> list[tuple[float, float, float]]:
    return [
        adjust_hsv(base_rgb, hue_shift=-0.035, sat_scale=0.72, val_scale=1.12, val_offset=0.03),
        adjust_hsv(base_rgb, hue_shift=0.025, sat_scale=0.96, val_scale=1.10, val_offset=0.02),
        adjust_hsv(base_rgb, hue_shift=0.0, sat_scale=1.00, val_scale=1.04),
        adjust_hsv(base_rgb, hue_shift=-0.02, sat_scale=0.92, val_scale=0.92),
        adjust_hsv(base_rgb, hue_shift=0.015, sat_scale=1.04, val_scale=0.66),
    ]


def create_round_rect(canvas: tk.Canvas, x0: float, y0: float, x1: float, y1: float, radius: float, **kwargs) -> int:
    radius = max(0.0, min(radius, (x1 - x0) / 2.0, (y1 - y0) / 2.0))
    points = [
        x0 + radius, y0,
        x1 - radius, y0,
        x1, y0,
        x1, y0 + radius,
        x1, y1 - radius,
        x1, y1,
        x1 - radius, y1,
        x0 + radius, y1,
        x0, y1,
        x0, y1 - radius,
        x0, y0 + radius,
        x0, y0,
    ]
    return canvas.create_polygon(points, smooth=True, splinesteps=18, **kwargs)


