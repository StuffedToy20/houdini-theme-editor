def guess_default_path() -> Path | None:
    candidates = sorted(path for path in Path.cwd().glob("*.hcs") if path.is_file())
    if len(candidates) == 1:
        return candidates[0]
    return None


def parse_args(argv: Iterable[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Edit Houdini .hcs theme files with a simple UI preview.")
    parser.add_argument("theme", nargs="?", help="Path to a .hcs file")
    return parser.parse_args(list(argv))


def main(argv: Iterable[str]) -> int:
    args = parse_args(argv)
    path = Path(args.theme).expanduser() if args.theme else guess_default_path()
    root = tk.Tk()
    HCSThemeEditorApp(root, path)
    root.mainloop()
    return 0


